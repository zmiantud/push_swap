int		ft_wordlen(const char *s, char c)
{
	int		w_len;
	int		i;

	w_len = 0;
	i = 0;
	if (s)
	{
		while (s[i] == c)
			i++;
		while (s[i] && s[i] != c)
		{
			w_len++;
			i++;
		}
	}
	return (w_len);
}
